const mongoose = require('mongoose');

const SeoBlogSchema = mongoose.Schema({
  meta_title: {
    az: { type: String, required: true },
    en: { type: String, required: true },
    ru: { type: String, required: true },
  },
  meta_description: {
    az: { type: String, required: true },
    en: { type: String, required: true },
    ru: { type: String, required: true },
  },
});

const SeoBlogModel = mongoose.model('blogseo', SeoBlogSchema);

module.exports = SeoBlogModel;
